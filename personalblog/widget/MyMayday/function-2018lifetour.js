	date1223=(spday1223.getTime()-now1223.getTime())/(24*60*60*1000);
	date1223=Math.ceil(date1223);

	if (date1223 < 0)
  	{
  		date1223 = 0;
  	};

　	document.getElementById("1223").value = (date1223);

